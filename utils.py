def emoji_for_product(name):

    text = name.lower()

    frozen = False

    if "заморож" in text or "замороз" in text:
        frozen = True

    for key, emoji in PRODUCT_EMOJI.items():

        if key in text:

            if frozen:
                return f"❄️{emoji}"

            return emoji

    if frozen:
        return "❄️🍽"

    return "🍽"


