def emoji_for_product(name):

    text = name.lower()

    frozen = False

    if "заморож" in text or "замороз" in text:
        frozen = True

    found = []

    # 1 уровень — основной словарь
    for key, emoji in PRODUCT_EMOJI.items():

        if key in text and emoji not in found:
            found.append(emoji)

    # 2 уровень — умный словарь
    for key, emoji in SMART_EMOJI.items():

        if key in text and emoji not in found:
            found.append(emoji)

    # если ничего не нашли
    if not found:

        # эвристика по словам
        if "орех" in text:
            found.append("🥜")

        elif "крем" in text:
            found.append("🍦")

        elif "соус" in text:
            found.append("🥫")

        elif "сироп" in text:
            found.append("🍯")

        elif "паста" in text:
            found.append("🍝")

        elif "шокол" in text:
            found.append("🍫")

        else:
            import random
            found.append(random.choice(["🍽","🍴","🥣","🛒"]))

    # максимум 2 эмодзи
    result = "".join(found[:2])

    if frozen:
        return f"❄️{result}"

    return result


